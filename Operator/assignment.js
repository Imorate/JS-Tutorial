// Assignment operator
let a = 10;

// Assignment operator with arithmetic operators
console.log('Addition assignment a = 10 + 5 =', a += 5);
console.log('Subtraction assignment a = 10 - 5 =', a -= 5);
console.log('Multiplication assignment a = 10 * 5 =', a *= 5);
console.log('Division assignment a = 10 / 5 =', a /= 5);
console.log('Remainder assignment a = 10 % 5 =', a %= 5);